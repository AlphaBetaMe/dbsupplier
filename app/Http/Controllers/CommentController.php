     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, $timelineId)
    {
        // Validate the comment input
        $request->validate([
            'comments' => 'required|max:255',
        ]);

        // Assuming you have a Comment model with a relationship to Timeline
        $comment = new Comment();
        $comment->timeline_id = $timelineId;
        $comment->user_id = auth()->user()->id; // Assuming the comment is associated with the authenticated user
        $comment->comments = $request->input('comments');
        $comment->save();

        return redirect()->back()->with('success', 'Comment added successfully!');
    }

